 class Kalkulator {
    double value1;
    double value2;
    
    double tambahBilangan() {
        double hasil = value1 + value2;
        return hasil;
    }
    double kurangBilangan() {
        double hasil = value1 - value2;
        return hasil;
    }
    double kaliBilangan() {
        double hasil = value1 * value2;
        return hasil;
    }
    double bagiBilangan() {
        double hasil = value1 / value2;
        return hasil;
    }
    double sisaBilangan() {
        double hasil = value1 % value2;
        return hasil;
    }
    
    
}